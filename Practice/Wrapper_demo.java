// Advantages of Java Wrapper Class : 
// They are used to convert the primitive data types into objects
// Objects are needed if we wish to modify the arguments passed into a method (because primitive types are passed by value).
// The package java.util contains classes which only handles objects, so it helps in this case too.
// In multithreading, we need an object to support synchronization.

public class Wrapper_demo {
    public static void main(String[] args) {

        Character a ;
        char b;
        byte c;
        Byte d;
        Float f;
        float g;
        short x;
        Short X;     

    }
}